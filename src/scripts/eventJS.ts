const countryLists: string[][][] = [
    WW1countryData,
    WW2countryData
]

let currentCountry: number = 0;

function nextCountry(direction: Direction, event: SelectedEvent){
    const list: string[][] = countryLists[event] as string[][];

    currentCountry += direction;
    if(currentCountry < 0){
        currentCountry = list.length-1;
    }
    else if(currentCountry > list.length-1){
        currentCountry = 0;
    }

    const country: string[] = list[currentCountry] as string[];

    changeLayout(country);
}

function changeLayout(country: string[]){
    const header: HTMLElement = document.getElementById('grid-Header-text');
    const img: HTMLImageElement = document.getElementById('gridBigImg') as HTMLImageElement;
    const flag: HTMLImageElement = document.getElementById('gridFlag') as HTMLImageElement;
    const text: HTMLElement = document.getElementById('gridText');

    header.innerText = country[0];
    img.src = country[1];
    flag.src = country[2];
    text.innerText = country[3];
}

document.addEventListener('DOMContentLoaded', ()=>{
    const header: string = document.getElementById('introHeader-content').innerText;
    switch (header){
        case "The Second World War":
            nextCountry(Direction.None, SelectedEvent.WW2);
            break;
        case "The First World War":
            nextCountry(Direction.None, SelectedEvent.WW1);
            break;
        default:
            break;
    }
})