enum Direction {
    Left = -1,
    Right = 1,
    None = 0
}

const countryData: string[][] = [
    franceData,
    germanyData,
    ukData,
    russiaData,
    kukData,
    ottoData,
    italyData,
    usData,
    bulgariaData,
    serbiaData
]

let currentCountry: number = 0;

function nextCountry(direction: Direction){
    currentCountry += direction;
    if(currentCountry < 0){
        currentCountry = countryData.length-1;
    }
    else if(currentCountry > countryData.length-1){
        currentCountry = 0;
    }

    const country: string[] = countryData[currentCountry] as string[];

    changeLayout(country);
}

function changeLayout(country: string[]){
    const header: HTMLElement = document.getElementById('grid-Header-text');
    const img: HTMLImageElement = document.getElementById('gridBigImg') as HTMLImageElement;
    const flag: HTMLImageElement = document.getElementById('gridFlag') as HTMLImageElement;
    const text: HTMLElement = document.getElementById('gridText');

    header.innerText = country[0];
    img.src = country[1];
    flag.src = country[2];
    text.innerText = country[3];
}

document.addEventListener('DOMContentLoaded', ()=>{
    nextCountry(Direction.None);
})